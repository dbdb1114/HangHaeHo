create DATABASE hanghaeho;

use hanghaeho;
-- hanghaeho.genre definition

CREATE TABLE `genre`
(
    `create_at` datetime(6)  NOT NULL,
    `genre_id`  bigint       NOT NULL AUTO_INCREMENT,
    `modify_at` datetime(6)  DEFAULT NULL,
    `create_by` varchar(255) NOT NULL,
    `modify_by` varchar(255) DEFAULT NULL,
    `name`      varchar(255) NOT NULL,
    PRIMARY KEY (`genre_id`)
) ENGINE = InnoDB
  AUTO_INCREMENT = 11
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_0900_ai_ci;


-- hanghaeho.rating definition

CREATE TABLE `rating`
(
    `create_at` datetime(6)  NOT NULL,
    `modify_at` datetime(6)  DEFAULT NULL,
    `rating_id` bigint       NOT NULL AUTO_INCREMENT,
    `create_by` varchar(255) NOT NULL,
    `img`       varchar(255) NOT NULL,
    `modify_by` varchar(255) DEFAULT NULL,
    `name`      varchar(255) NOT NULL,
    PRIMARY KEY (`rating_id`)
) ENGINE = InnoDB
  AUTO_INCREMENT = 6
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_0900_ai_ci;


-- hanghaeho.screen definition

CREATE TABLE `screen`
(
    `create_at` datetime(6)  NOT NULL,
    `modify_at` datetime(6)  DEFAULT NULL,
    `screen_id` bigint       NOT NULL AUTO_INCREMENT,
    `create_by` varchar(255) NOT NULL,
    `modify_by` varchar(255) DEFAULT NULL,
    `name`      varchar(255) DEFAULT NULL,
    PRIMARY KEY (`screen_id`)
) ENGINE = InnoDB
  AUTO_INCREMENT = 28
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_0900_ai_ci;


-- hanghaeho.movie definition

