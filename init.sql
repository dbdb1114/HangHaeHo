    `seat_id`       bigint                                  NOT NULL,
    `showing_id`    bigint                                  NOT NULL,
    `ticket_id`     bigint                                  NOT NULL AUTO_INCREMENT,
    `create_by`     varchar(255)                            NOT NULL,
    `modify_by`     varchar(255) DEFAULT NULL,
